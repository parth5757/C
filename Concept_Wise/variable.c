// Variables are containers for storing data values, like numbers and characters.

// In c there are different types of variable like as follow.
// -   int
// -   float
// -   char
// -   & more.

// Syntax
// type variableName = value;

// Example
#include<stdio.h>
int main(){
    int myNum = 15;
    printf("%d\n", myNum); // %d is format specifier(here use for the print integer value.), \n is use for the new line printing.

    // Variable using declare.
    int Num;
    Num = 20;
    printf("%d", Num);

    return 0;
}